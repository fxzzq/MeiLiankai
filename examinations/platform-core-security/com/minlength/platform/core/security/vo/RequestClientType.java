package com.minlength.platform.core.security.vo;

/**
 * 客户端的类型
 * @author shugang
 *
 */
public enum RequestClientType {
	//PC端
	PC(1),
	//移动手机端
	MOBILE(2),
	//微信内置浏览器
	WEIXIN(3)
	;
	private int type;
	RequestClientType(int type) {
		this.type = type;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
}
